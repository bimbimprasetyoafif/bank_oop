    private void inputPenarikanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputPenarikanButtonActionPerformed
              
        double masukan = Double.parseDouble(inputPenarikanPane.getText());
        if(masukan > acc.getSaldo()){
            JOptionPane.showMessageDialog(rootPane,"Maaf Saldo anda kurang.");
        }
        else{
            double admin, maks;
            if(acc.getJenis() == 's') {
                maks = 2500000;
                admin = 2000;
            } else if(acc.getJenis() == 'g') {
                maks = 5000000;
                admin = 5000;
            } else {
                maks = 7000000;
                admin = 7000;
            }
            
            if(masukan > maks) {
                JOptionPane.showMessageDialog(null, "Maksimal Penarikan "+ maks);
            } else {
                acc.penarikanSaldo(admin);
                acc.penarikanSaldo(masukan);
                JOptionPane.showMessageDialog(rootPane,"Penarikan Berhasil.\nBiaya Admin : "+ admin +"\nSaldo Anda Saat Ini adalah :"+ acc.getSaldo()+ "\n");
            }
        }
        penarikanPanel.setVisible(false);
        transaksiPanel.setVisible(true);
    }//GEN-LAST:event_inputPenarikanButtonActionPerformed

