    private void cekButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekButtonActionPerformed
        if(bank.terdapatNoRekening(nomorTujuanPane.getText()) == false) {
            JOptionPane.showMessageDialog(rootPane, "Nomor Rekening tersebut Tidak Tersedia");
        } else {
            if(nomorTujuanPane.getText().equals(noker_pengirim))
                JOptionPane.showMessageDialog(rootPane, "Tidak Bisa Mengirim ke Nomor Rekening Anda Sendiri.");
            else {
                noker_tujuan = nomorTujuanPane.getText();
                tujuan = (Savings) bank.getAkun(nomorTujuanPane.getText());
                namaTujuanLabel.setText(tujuan.getNama());
            }
        }
    }//GEN-LAST:event_cekButtonActionPerformed

    private void accButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accButtonActionPerformed
        double masukan = Double.parseDouble(jumlahPane.getText());
        if(masukan > acc.getSaldo()) {
            JOptionPane.showMessageDialog(rootPane,"Maaf Saldo anda kurang.");
        }
        else{
            double admin, maks;
            if(acc.getJenis() == 's') {
                maks = 2500000;
                admin = 2000;
            } else if(acc.getJenis() == 'g') {
                maks = 5000000;
                admin = 5000;
            } else {
                maks = 7000000;
                admin = 7000;
            }
            
            if(masukan > maks) {
                JOptionPane.showMessageDialog(rootPane, "Maksimal Jumlah Transfer "+ maks);
            } else {
                acc.penarikanSaldo(masukan);
                tujuan.setSaldo(masukan);
                acc.penarikanSaldo(admin);
                JOptionPane.showMessageDialog(rootPane,"Transfer Kepada" + tujuan.getNama() +" Berhasil.\nBiaya Admin : "+ admin +".\nSaldo Anda Saat Ini adalah :"+ acc.getSaldo()+ "\n");
            }
        }
        transferPanel.setVisible(false);
        transaksiPanel.setVisible(true);
    }//GEN-LAST:event_accButtonActionPerformed

