    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(bank.terdapatNoRekening(nmrTujuanTextPane.getText()) == false){
           JOptionPane.showMessageDialog(rootPane, "Nomor Rekening Tidak Tersedia!");
       } else {
           norek_tujuan = nmrTujuanTextPane.getText();
           tujuanPanel.setVisible(false);
           tujuanSearchPanel.setVisible(true);
           tujuan = (Savings) bank.getAkun(nmrTujuanTextPane.getText());
           noKerDestLabel.setText(norek_tujuan);
           namaDestLabel.setText(tujuan.getNama());
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void transferButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferButtonActionPerformed
        double masukan = Double.parseDouble(jumlahTextPane.getText());
        
        if(masukan > acc.getSaldo()) {
            JOptionPane.showMessageDialog(rootPane, "Saldo Anda Tidak Cukup");
        } else {
            acc.setSaldo(masukan);
            tujuan.penarikanSaldo(masukan);
            JOptionPane.showMessageDialog(rootPane, "Transfer Berhasil.\nDari "+ namaLabel.getText() +" Kepada "+ namaDestLabel.getText() + " sebesar Rp "+ jumlahTextPane.getText());
            this.dispose();
        }
    }//GEN-LAST:event_transferButtonActionPerformed

