    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(bank.terdapatNoRekening(cariTextPane.getText()) == false) {
            JOptionPane.showMessageDialog(rootPane, "Nomor Rekening Tidak Terdapat");
        } else {
            norek_pengirim = cariTextPane.getText();
            searchPanel.setVisible(false);
            transferPanel.setVisible(true);
            acc = (Savings) bank.getAkun(cariTextPane.getText());
            noKerLabel.setText(norek_pengirim);
            namaLabel.setText(acc.getNama());
            saldoLabel.setText(String.valueOf(acc.getSaldo()));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

